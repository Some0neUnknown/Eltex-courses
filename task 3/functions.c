#include <stdio.h>

int sum(int number1, int number2 ){
    int result = number1 + number2;
    return result;
}

int sub(int number1, int number2){
    int result = number1 - number2;
    return result;
}

int mul(int number1, int number2){
    int result = number1 * number2;
    return result;
}

int dif(int number1, int number2){
    float result = number1 / number2;
    return result;
}

